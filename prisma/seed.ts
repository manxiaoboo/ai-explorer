async function main() {
  console.log('ðŸŒ± Starting database seed...\n');

  // Clear existing data
  console.log('Clearing existing data...');
  await prisma.priceHistory.deleteMany();
  await prisma.tool.deleteMany();
  await prisma.category.deleteMany();
  await prisma.tag.deleteMany();
  console.log('âœ… Data cleared\n');

  // Create categories
  console.log('Creating categories...');
  const categoryMap = new Map();
  for (const category of categories) {
    const created = await prisma.category.create({
      data: {
        slug: category.slug,
        name: category.name,
        description: category.description,
        icon: category.icon,
        sortOrder: category.sortOrder,
      },
    });
    categoryMap.set(category.slug, created.id);
    console.log(`  âœ… ${category.name}`);
  }
  console.log('');

  // Create tags
  console.log('Creating tags...');
  const tagMap = new Map();
  for (const tagName of tags) {
    const created = await prisma.tag.create({
      data: { name: tagName },
    });
    tagMap.set(tagName, created.id);
  }
  console.log(`  âœ… ${tags.length} tags created\n`);

  // Create tools
  console.log('Creating tools...');
  for (const tool of tools) {
    const categoryId = categoryMap.get(tool.categorySlug);
    if (!categoryId) {
      console.log(`  âš ï¸ Skipping ${tool.name} - category not found`);
      continue;
    }

    // Assign random tags (3-5 tags per tool)
    const toolTagNames = tags
      .sort(() => Math.random() - 0.5)
      .slice(0, Math.floor(Math.random() * 3) + 3);

    const toolTagIds = toolTagNames
      .map(name => tagMap.get(name))
      .filter((id): id is string => !!id);

    await prisma.tool.create({
      data: {
        slug: tool.slug,
        name: tool.name,
        tagline: tool.tagline,
        description: tool.description,
        website: tool.website,
        categoryId: categoryId,
        pricingTier: tool.pricingTier,
        priceStart: tool.priceStart,
        hasFreeTier: tool.hasFreeTier,
        hasTrial: tool.hasTrial,
        features: tool.features,
        useCases: tool.useCases,
        githubStars: tool.githubStars,
        productHuntVotes: tool.productHuntVotes,
        trendingScore: tool.trendingScore,
        isFeatured: tool.isFeatured,
        isActive: tool.isActive,
        Tag: {
          connect: toolTagIds.map(id => ({ id })),
        },
      },
    });
    console.log(`  âœ… ${tool.name}`);
  }
  console.log('');

  // Create price history for some tools
  console.log('Creating price history...');
  const allTools = await prisma.tool.findMany();
  for (const tool of allTools.slice(0, 10)) {
    if (tool.priceStart) {
      await prisma.priceHistory.create({
        data: {
          toolId: tool.id,
          price: tool.priceStart * (0.8 + Math.random() * 0.4),
          pricingTier: tool.pricingTier,
        },
      });
    }
  }
  console.log('âœ… Price history created\n');

  // Summary
  const stats = await Promise.all([
    prisma.category.count(),
    prisma.tool.count(),
    prisma.tag.count(),
  ]);

  console.log('ðŸ“Š Seed completed!');
  console.log(`   Categories: ${stats[0]}`);
  console.log(`   Tools: ${stats[1]}`);
  console.log(`   Tags: ${stats[2]}`);
}

main()
  .catch((e) => {
    console.error('âŒ Seed failed:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
